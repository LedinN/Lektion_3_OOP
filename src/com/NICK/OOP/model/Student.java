package com.NICK.OOP.model;

public class Student {
public String name;
public int age;
public boolean wantCoffee;


    //Constructor
    // Value = instantiation param
    public Student(String name, int age, boolean wantCoffee) {
        this.name = name;
        this.age = age;
        this.wantCoffee = wantCoffee;
    }
}

